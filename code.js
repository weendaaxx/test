let currentQuestion = 0;
let score = 0;

const question = document.getElementById("question");
const choices = document.getElementById("choices");
const nextBtn = document.getElementById("nextBtn");

function loadQuestion() {

    nextBtn.style.display = "none";

    feedback.style.display = "none";
    feedback.innerHTML = "";

    question.textContent =
        `${currentQuestion + 1}. ${quiz[currentQuestion].question}`;

    choices.innerHTML = "";

    quiz[currentQuestion].choices.forEach((choice, index) => {

        const btn = document.createElement("button");

        btn.textContent = choice;
        btn.className = "choice";

        btn.addEventListener("click", () => checkAnswer(index, btn));

        choices.appendChild(btn);

    });

}

const feedback = document.getElementById("feedback");

function checkAnswer(selected, button){

    const buttons = document.querySelectorAll(".choice");

    buttons.forEach(btn => btn.disabled = true);

    feedback.style.display = "block";

    if(selected === quiz[currentQuestion].answer){

        score++;

        button.style.background = "green";
        button.style.color = "white";

        feedback.className = "feedback correct";

        feedback.innerHTML = `
            <strong>✅ Correct!</strong>

            <b>Explanation:</b><br>
            ${quiz[currentQuestion].explanation}
        `;

    }else{

        button.style.background = "red";
        button.style.color = "white";

        buttons[quiz[currentQuestion].answer].style.background = "green";
        buttons[quiz[currentQuestion].answer].style.color = "white";

        feedback.className = "feedback wrong";

        feedback.innerHTML = `
            <strong>❌ Incorrect!</strong>

            <b>Correct Answer:</b>
            ${quiz[currentQuestion].choices[quiz[currentQuestion].answer]}

            <br><br>

            <b>Explanation:</b><br>
            ${quiz[currentQuestion].explanation}
        `;
    }

    nextBtn.style.display = "block";
}
nextBtn.addEventListener("click", () => {

    currentQuestion++;

    if (currentQuestion < quiz.length) {

        loadQuestion();

    } else {

        document.querySelector(".quiz-container").innerHTML = `
            <div id="result">
                <h2>Quiz Finished!</h2>
                <h3>Your Score</h3>
                <h1>${score} / ${quiz.length}</h1>
                <button onclick="location.reload()">Try Again</button>
            </div>
        `;

    }

});
loadQuestion();